def _invoke_code_model(prompt: str) -> str:
    try:
        response = client.chat.completions.create(
            model=CODEGEN_MODEL,
            messages=[
                {"role": "system", "content": "You output strict JSON only."},
                {"role": "user", "content": prompt},
            ],
            temperature=0.15,
            response_format={"type": "json_object"},
        )
        return (response.choices[0].message.content or "").strip()
    except BadRequestError as exc:
        body = getattr(exc, "body", None)
        if isinstance(body, dict):
            error = body.get("error", {})
            if error.get("code") == "json_validate_failed":
                failed_generation = error.get("failed_generation", "")
                if failed_generation:
                    return str(failed_generation).strip()
        raise


def _extract_json_candidate(raw_content: str) -> str:
    fenced = raw_content.strip()
    if fenced.startswith("```"):
        fenced = fenced.strip("`")
        if fenced.startswith("json"):
            fenced = fenced[4:]
    match = JSON_OBJECT_PATTERN.search(fenced)
    return match.group(0) if match else raw_content


def _escape_control_chars_in_json_strings(text: str) -> str:
    """Repair common LLM JSON issues: raw newlines/tabs inside quoted strings."""
    out: list[str] = []
    in_string = False
    escaped = False

    for char in text:
        if in_string:
            if escaped:
                out.append(char)
                escaped = False
                continue

            if char == "\\":
                out.append(char)
                escaped = True
                continue

            if char == '"':
                out.append(char)
                in_string = False
                continue

            if char == "\n":
                out.append("\\n")
                continue
            if char == "\r":
                out.append("\\r")
                continue
            if char == "\t":
                out.append("\\t")
                continue

            out.append(char)
            continue

        out.append(char)
        if char == '"':
            in_string = True

    return "".join(out)


def _normalize_files(files_obj: Any) -> dict[str, str]:
    if not isinstance(files_obj, dict):
        raise ValueError("`files` must be an object mapping filepath -> code")

    normalized: dict[str, str] = {}
    for path, content in files_obj.items():
        file_path = str(path).strip().replace("\\", "/")
        if not file_path:
            continue
        # Avoid absolute and parent traversal paths.
        if file_path.startswith("/") or ".." in file_path.split("/"):
            continue
        normalized[file_path] = str(content)

    if not normalized:
        raise ValueError("No valid files found in model output")

    return normalized


def _normalize_test_cases(items: Any) -> list[dict[str, Any]]:
    if not isinstance(items, list):
        return []

    normalized: list[dict[str, Any]] = []
    for item in items:
        if isinstance(item, dict):
            normalized.append(item)
    return normalized


def _is_python_test_file(path: str) -> bool:
    normalized = str(path).replace("\\", "/").strip("/")
    if not normalized:
        return False
    lowered = normalized.lower()
    name = lowered.rsplit("/", 1)[-1]
    return (
        lowered.startswith("tests/")
        or "/tests/" in lowered
        or name.startswith("test_")
        or name.endswith("_test.py")
    )


def _extract_pytest_counts(output: str) -> dict[str, int]:
    counts = {
        "passed": 0,
        "failed": 0,
        "errors": 0,
        "skipped": 0,
        "xfailed": 0,
        "xpassed": 0,
    }
    if not output:
        return counts

    summary_line = ""
    for raw_line in reversed(output.splitlines()):
        line = raw_line.strip()
        if not line:
            continue
        if " in " not in line:
            continue
        if not re.search(r"\b(passed|failed|error|errors|skipped|xfailed|xpassed)\b", line, re.IGNORECASE):
            continue
        summary_line = line
        break

    if not summary_line:
        return counts

    for label in counts:
        match = re.search(rf"(\d+)\s+{label}", summary_line, re.IGNORECASE)
        if match:
            counts[label] = int(match.group(1))

    return counts


def _parse_code_response(raw_content: str) -> dict[str, str]:
    try:
        payload = parse_llm_json(raw_content)
    except Exception:
        candidate = _extract_json_candidate(raw_content)
        repaired = _escape_control_chars_in_json_strings(candidate)
        payload = json.loads(repaired)

    if isinstance(payload, dict) and "files" in payload:
        return _normalize_files(payload["files"])
    if isinstance(payload, dict):
        # lenient fallback if model returned direct file map
        return _normalize_files(payload)
    raise ValueError("Model response must be a JSON object with a `files` key")


def _parse_test_response(raw_content: str) -> dict[str, Any]:
    try:
        payload = parse_llm_json(raw_content)
    except Exception:
        candidate = _extract_json_candidate(raw_content)
        repaired = _escape_control_chars_in_json_strings(candidate)
        payload = json.loads(repaired)

    if not isinstance(payload, dict):
        raise ValueError("Test generation response must be a JSON object")

    return {
        "unit_test_files": _normalize_files(payload.get("unit_test_files", {})),
        "manual_test_cases": _normalize_test_cases(payload.get("manual_test_cases", [])),
        "automated_test_cases": _normalize_test_cases(payload.get("automated_test_cases", [])),
    }


def _story_text(story: dict[str, Any]) -> str:
    parts: list[str] = []
    for key in ("title", "summary", "description", "acceptance_criteria", "definition_of_done", "details"):
        value = story.get(key)
        if isinstance(value, list):
            parts.extend(str(item).strip() for item in value if str(item).strip())
        elif value is not None:
            text = str(value).strip()
            if text:
                parts.append(text)
    return " ".join(parts).lower()


def _auth_story_scope(story: dict[str, Any]) -> str | None:
    text = _story_text(story)
    if not any(keyword in text for keyword in ("auth", "login", "log in", "register", "sign up", "signup", "logout", "session")):
        return None
    if any(keyword in text for keyword in ("register", "registration", "sign up", "signup", "sign-up")):
        return "register"
    if any(keyword in text for keyword in ("logout", "log out", "sign out", "signout", "sign-out")):
        return "logout"
    if "session" in text:
        return "session"
    if any(keyword in text for keyword in ("login", "log in", "sign in", "signin", "sign-in")):
        return "login"
    return "auth"


