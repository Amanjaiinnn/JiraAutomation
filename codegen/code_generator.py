def _story_lines(story: dict[str, Any], key: str) -> list[str]:
    value = story.get(key, [])
    if isinstance(value, list):
        return [str(item).strip() for item in value if str(item).strip()]
    if value is None:
        return []
    text = str(value).strip()
    return [text] if text else []


def _story_section(story: dict[str, Any], key: str, fallback: str = "Not provided.") -> str:
    lines = _story_lines(story, key)
    if not lines:
        return fallback
    return "\n".join(f"- {line}" for line in lines)


def _ui_reference_section(story: dict[str, Any]) -> str:
    reference = story.get("ui_reference")
    if not isinstance(reference, dict):
        return "No explicit UI prompt provided."

    text = str(reference.get("text") or "").strip()
    image_name = str(reference.get("image_name") or "").strip()
    parts: list[str] = []
    if text:
        parts.append(f"Visualization notes:\n{text}")
    if image_name:
        parts.append(f"Reference screenshot attached: {image_name}")
    return "\n".join(parts) if parts else "No explicit UI prompt provided."


def _normalize_text(value: Any) -> str:
    return " ".join(str(value or "").split()).strip()


def _normalize_list(values: Any) -> list[str]:
    if not isinstance(values, list):
        return []
    return [_normalize_text(item) for item in values if _normalize_text(item)]


def _extract_ui_reference_hints(story: dict[str, Any]) -> dict[str, Any]:
    reference = story.get("ui_reference")
    if not isinstance(reference, dict):
        return {
            "text": "",
            "image_name": "",
            "layout_hints": [],
            "interaction_hints": [],
            "component_hints": [],
        }

    text = _normalize_text(reference.get("text"))
    image_name = _normalize_text(reference.get("image_name"))
    lower_text = text.lower()

    layout_hints: list[str] = []
    interaction_hints: list[str] = []
    component_hints: list[str] = []

    hint_map = {
        "kanban": ("Kanban-style multi-column board layout", "drag-and-drop board interactions", "column board"),
        "drag and drop": ("drag-aware task layout", "drag-and-drop interactions", "draggable cards"),
        "drag-and-drop": ("drag-aware task layout", "drag-and-drop interactions", "draggable cards"),
        "table": ("tabular data layout", "", "data table"),
        "dashboard": ("dashboard summary layout", "", "summary cards"),
        "sidebar": ("sidebar navigation layout", "", "sidebar"),
        "modal": ("modal-based editing flow", "modal open/close flows", "modal dialog"),
        "three columns": ("three-column content layout", "", "three-column board"),
        "cards": ("card-based content layout", "", "card grid"),
        "form": ("structured form layout", "form validation flow", "validated forms"),
        "filter": ("filterable workspace layout", "filter and search interactions", "filter controls"),
        "search": ("search-first layout", "search interactions", "search input"),
        "tabs": ("tabbed workflow layout", "tab switching interactions", "tab navigation"),
    }

    for key, hints in hint_map.items():
        if key in lower_text:
            if hints[0] and hints[0] not in layout_hints:
                layout_hints.append(hints[0])
            if hints[1] and hints[1] not in interaction_hints:
                interaction_hints.append(hints[1])
            if hints[2] and hints[2] not in component_hints:
                component_hints.append(hints[2])

    if image_name and "screen" not in component_hints:
        component_hints.append("screen hierarchy aligned with the attached reference")

    return {
        "text": text,
        "image_name": image_name,
        "layout_hints": layout_hints,
        "interaction_hints": interaction_hints,
        "component_hints": component_hints,
    }


